package com.ashwin;

public class UsingVariablePrimitiveDataType {

	/**
	 * Java Using Variable
	 * @param args
	 */
	public static void main(String[] args) {
		
		// 8 Primitive Data Type in Java
		
		// Declare Variable
			int myNumber;
			short myShortNum;
			long myLong;
			
			double myDouble;
			float myFloat;
			
			char myChar;
			boolean myBoolean;
			
			byte myByte;
		
		// Initialize Variable
			myNumber = 2017;
			myShortNum = 123;
			myLong = 9797;
			
			myDouble = 7.123123;
			myFloat = 324.3f;
			
			myChar = 'A';
			myBoolean = true;
			
			myByte = 127;
					
		// Print Variable
			System.out.println("My Number is: " + myNumber);
			System.out.println("My Short Number is: " + myShortNum);
			System.out.println("My Long Number is: " + myLong);
			System.out.println("My Double Number is: " + myDouble);
			System.out.println("My Float Number is: " + myFloat);
			System.out.println("My Char is: " + myChar);
			System.out.println("My Boolean is: " + myBoolean);
			System.out.println("My Byte is: " + myByte);

	}

}
